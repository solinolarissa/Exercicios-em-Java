    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {
        if (Codigo.getText().equals("") || Nome.getText().equals("") || CH.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Erro campo vazio");
        } else {
            Disciplina novo = new Disciplina(Codigo.getText(), Nome.getText(), CH.getText());
            Main.ListadeDisciplina.add(novo);
            JOptionPane.showMessageDialog(null, "Cadastro efetuado com sucesso");
            Codigo.setText("");
            Nome.setText("");
            CH.setText("");
        }
    }

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {
        Main.telaCadastrDisciplina.setVisible(false);
        Main.telaMenu.setVisible(true);
    }
 
